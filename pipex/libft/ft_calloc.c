#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	size_t		totalsize;
	void		*ptr;

	totalsize = nmemb * size;
	ptr = malloc(totalsize);
	if (!ptr)
		return (NULL);
	ft_bzero(ptr, totalsize);
	return (ptr);
}
